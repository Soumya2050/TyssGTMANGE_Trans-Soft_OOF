package stringMethods;

public class StartsWith_and_endsWith_method {
	
	public static void main(String[] args) {
		
		String s="soumyaranjan";
		/**
		 * endsWith() is basically check wheather the last specified character is present or not and it returns boolean result
		 */
		System.out.println(s.endsWith("s"));
		
		/**
		 * startsWith() is basically check wheather the first specified character is present or not and it returns boolean result
		 */
		
		System.out.println(s.startsWith("s"));
	}

}
